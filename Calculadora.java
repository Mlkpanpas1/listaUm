package calculadora;


public class Calculadora {


	private int a;
	private int b;
  
   public int soma () {
       return a + b;
   }
 
   public int subtrair () {
       return a - b;
   }
   public int multiplicar () {
       return a * b;
   }
 
   public double dividir () {
       return a / b;
   }
}
